export function t(lang, key, vars) {
  const entry = translations[key];
  if (!entry) return key;
  let str = entry[lang] || entry.nl || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replaceAll(`{{${k}}}`, v);
    }
  }
  return str;
}
